"""
Data types represent the kind of value a particular variable can hold. 

1] Integer (int): Represents whole numbers, e.g., 5, -3, 0.
2] Float (float): Represents floating-point numbers, i.e., numbers with a decimal point, e.g., 3.14, -0.001.
3] String (str): Represents sequences of characters enclosed within single, double, or triple quotes, e.g., "hello", 'Python', '''multiline string'''.
4] Boolean (bool): Represents either True or False.
5] List: Represents ordered sequences of items, e.g., [1, 2, 3], ['apple', 'banana', 'orange'].
6] Tuple: Similar to lists but immutable (cannot be changed), e.g., (1, 2, 3), ('a', 'b', 'c').
7] Dictionary (dict): Represents key-value pairs, e.g., {'name': 'John', 'age': 30}.
8] Set: Represents an unordered collection of unique items, e.g., {1, 2, 3}, {'apple', 'banana', 'orange'}.
"""